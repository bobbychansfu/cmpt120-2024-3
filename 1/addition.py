'''
adds two number and displays result
input - a,b integers
output - a+b
sample:
2
3
output: 5
'''

# ask user for a,b
a_int = int(input("Please enter an integer: "))   # ask user for input, put it into a
b_int = int(input("Please enter an integer: "))
# add a+b
sum_of_ab = a_int + b_int
# display result
print(sum_of_ab)
