
'''
  this program collects two numbers
  and prints the sum of the the numbers
  to the terminal (shell)
'''

# take the user's inputs and convert into float
first_input = float(input("Enter first number: "))
                   # take user's first input 
second_input = float(input("Enter second number: "))
                   # take user's second input


# add the user's inputs together to form answer
answer = first_input + second_input

# print the answer to the terminal
print("The sum of the two numbers is:", answer)


